package com.airhockey.android.util;

public class Geometrija {

	public static class Cestica {

		public float x;
		public float y;
		public float z;

		public Cestica(float x, float y, float z) {
			this.x = x;
			this.y = y;
			this.z = z;
		}
	}

	public static class Vektor {

		public float x;
		public float y;
		public float z;

		public Vektor(float x, float y, float z) {
			this.x = x;
			this.y = y;
			this.z = z;
		}
	}
}
