package com.airhockey.android.util;

public class Geometrija {

	public class Cestica {

		private float x;
		private float y;
		private float z;

		public Cestica(float x, float y, float z) {
			this.x = x;
			this.y = y;
			this.z = z;
		}
	}

	public class Vektor {

		private float x;
		private float y;

		public Vektor(float x, float y) {
			this.x = x;
			this.y = y;
		}
	}
}
